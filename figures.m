

% example 1
c = 7.98;
p = -c;
w= 2*pi;
q = 2*pi;
A2 = [];
A3 = [];
A4 = [];
A5 = [];

i = 1
for aa = -20:0.1:20
    A2(i) = double(a2(aa, c, w, p, q));
    A3(i) = double(a3(aa, c, w, p, q));
    A4(i) = double(a4(aa, c, w, p, q));
    A5(i) = double(a5(aa, c, w, p, q));
    i = i+1;
end 
plot(-20:0.1:20, A2, 'LineWidth', 2, 'DisplayName', 'A2')
hold on
plot(-20:0.1:20, A3, 'LineWidth', 2, 'DisplayName', 'A3')
plot(-20:0.1:20, A4, 'LineWidth', 2, 'DisplayName', 'A4')
plot(-20:0.1:20, A5, 'LineWidth', 2, 'DisplayName', 'A5')
hold off
legend('show', 'FontSize', 18);
title('c = -p = 7.98, w = q = 2*\pi', 'FontSize', 18);
print('example-1', '-djpeg', '-r300');

